
import streamlit as st
import pandas as pd
import matplotlib.pyplot as plt
from io import BytesIO
import base64
import xlsxwriter

# --- BLOQUE DE SEGURIDAD ---
st.set_page_config(page_title="Auditor√≠a de Gastos - FarmaValue", layout="wide")
password = st.text_input("Ingresa la contrase√±a para acceder a la aplicaci√≥n:", type="password")
if password != "Herson2025":
    st.warning("üîí Acceso restringido. Contrase√±a incorrecta.")
    st.stop()

# --- INTERFAZ INICIAL ---
st.title("Auditor√≠a a Gastos por Pa√≠s - Grupo FarmaValue_Herson Hern√°ndez")
archivo = st.file_uploader("Selecciona tu archivo de gastos", type=["xlsx"])

if archivo:
    df = pd.read_excel(archivo)
    df['Fecha'] = pd.to_datetime(df['Fecha'])
    df['Mes'] = df['Fecha'].dt.strftime('%B')
    meses_orden = ['January', 'February', 'March', 'April']
    df['Mes'] = pd.Categorical(df['Mes'], categories=meses_orden, ordered=True)

    resumen_mes = df.groupby('Mes')['Monto'].sum().reindex(meses_orden)
    st.bar_chart(resumen_mes)

    # --- L√ìGICA DE REVISI√ìN INTELIGENTE ---
    df['Gasto Total Sucursal Mes'] = df.groupby(['Sucursales', 'Mes'])['Monto'].transform('sum')
    df['% Participaci√≥n'] = (df['Monto'] / df['Gasto Total Sucursal Mes']) * 100

    def clasificar_riesgo(row):
        if row['Monto'] >= 2000000 or row['% Participaci√≥n'] >= 12:
            return "üî¥ Cr√≠tico"
        elif row['Monto'] >= 1000000:
            return "üü° Moderado"
        else:
            return "üü¢ Bajo"

    df['Grupo_Riesgo'] = df.apply(clasificar_riesgo, axis=1)

    criterios_snack = df['Descripcion'].str.contains("comida|snack|sin comprobante|misc|varios", case=False, na=False)
    criterios_seguro = df['Descripcion'].str.contains("recuperaci√≥n|seguro|diferencia|no cobrados|ajuste|reclasificaci√≥n|cxc", case=False, na=False)

    frecuencia = df.groupby(['Sucursales', 'Descripcion'])['Monto'].transform('count')
    criterios_repetidos = frecuencia >= 3

    df['¬øRevisar?'] = (
        (df['Monto'] >= 2000000) |
        (df['% Participaci√≥n'] >= 12) |
        criterios_snack |
        criterios_seguro |
        criterios_repetidos
    ).map({True: "S√≠", False: "No"})

    df['Monto del Gasto'] = df['Monto'].round(2)
    df['Gasto Total de la Sucursal'] = df['Gasto Total Sucursal Mes'].round(2)
    df['% Participaci√≥n'] = df['% Participaci√≥n'].round(2)
    df['Verificado (‚òê)'] = ""
    df['No Verificado (‚òê)'] = ""
    df['Comentario del Auditor'] = ""

    # Colorear seguros en rojo (solo visual en Excel)
    def formato_descripcion(val):
        if isinstance(val, str) and any(x in val.lower() for x in ["seguro", "cxc", "ars", "aseguradora", "reembolso"]):
            return 'color: red'
        return ''

    columnas = [
        'Sucursales', 'Grupo_Riesgo', 'Categoria', 'Descripcion', 'Fecha',
        'Monto del Gasto', 'Gasto Total de la Sucursal', '% Participaci√≥n',
        '¬øRevisar?', 'Verificado (‚òê)', 'No Verificado (‚òê)', 'Comentario del Auditor'
    ]
    columnas_existentes = [col for col in columnas if col in df.columns]

    if len(columnas_existentes) < len(columnas):
        st.error("‚ùå Algunas columnas necesarias no existen en el archivo.")
    else:
        cedula = df[columnas_existentes].sort_values(by='% Participaci√≥n', ascending=False)

        # --- DESCARGA DEL ARCHIVO ---
        def generar_excel():
            output = BytesIO()
            with pd.ExcelWriter(output, engine='xlsxwriter') as writer:
                wb = writer.book
                format_title = wb.add_format({'bold': True, 'font_size': 28, 'font_color': 'red'})
                format_sub = wb.add_format({'font_size': 12})

                cedula.to_excel(writer, sheet_name="C√©dula Auditor", startrow=5, index=False)
                ws = writer.sheets["C√©dula Auditor"]
                ws.write("A1", "Auditor√≠a grupo Farmavalue", format_title)
                ws.write("A2", "Reporte de gastos del 01 de Enero al 20 de abril del 2025", format_sub)
                ws.write("A3", "Auditor Asignado:", format_sub)
                ws.write("A4", "Fecha de la Auditor√≠a", format_sub)

                # Colorear descripciones sospechosas
                format_red = wb.add_format({'font_color': 'red'})
                desc_idx = columnas_existentes.index('Descripcion')
                for row_idx, val in enumerate(cedula['Descripcion'], start=5):
                    if any(x in str(val).lower() for x in ["seguro", "cxc", "ars", "aseguradora", "reembolso"]):
                        ws.write(row_idx, desc_idx, val, format_red)

            output.seek(0)
            return output

        st.download_button(
            label="üì• Descargar Excel C√©dula Auditor",
            data=generar_excel(),
            file_name="Cedula_Auditor_FINAL_OK.xlsx",
            mime="application/vnd.openxmlformats-officedocument.spreadsheetml.sheet"
        )
